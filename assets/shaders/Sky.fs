vec3 rgb2hsv(vec3 c) {
    vec4 K = vec4(0.0, -1.0 / 3.0, 2.0 / 3.0, -1.0);
    vec4 p = mix(vec4(c.bg, K.wz), vec4(c.gb, K.xy), step(c.b, c.g));
    vec4 q = mix(vec4(p.xyw, c.r), vec4(c.r, p.yzx), step(p.x, c.r));

    float d = q.x - min(q.w, q.y);
    float e = 1.0e-10;
    return vec3(abs(q.z + (q.w - q.y) / (6.0 * d + e)), d / (q.x + e), q.x);
}

vec3 hsv2rgb(vec3 c) {
    vec4 K = vec4(1.0, 2.0 / 3.0, 1.0 / 3.0, 3.0);
    vec3 p = abs(fract(c.xxx + K.xyz) * 6.0 - K.www);
    return c.z * mix(K.xxx, clamp(p - K.xxx, 0.0, 1.0), c.y);
}

vec3 desaturate(vec3 color, float amount) {
    vec3 gray = vec3(dot(vec3(0.2126,0.7152,0.0722), color));
    return vec3(mix(color, gray, amount));
}

vec3 offsetColor(float noise) {
    vec3 c = rgb2hsv(color);
    c.x += 0.25;
    c = hsv2rgb(c);
    return c;
}

void main() {
    float fNoise = snoise((vPos * 0.01) + time * 10.0);
    float noise = range(fNoise, -1.0, 1.0, 0.0, 1.0);

    vec3 color0 = color;
    vec3 color1 = offsetColor(fNoise);

    vec3 finalColor = mix(color0, color1, noise);

    finalColor *= clamp(range(abs(vPos.y), 0.0, 25.0, 1.2, 1.0), 1.0, 15.0);

    finalColor = rgb2hsv(finalColor);
    finalColor.z -= range(sat, 0.0, 1.0, 0.0, 0.7);
    finalColor = hsv2rgb(finalColor);

    gl_FragColor = vec4(finalColor, 1.0);
}
